      function getOutletDetails($pdo, $outletId)
      {
        $stmt = $pdo->prepare("SELECT * FROM outlets WHERE outlet_id = ?");
        $stmt->execute([$outletId]);
        return $stmt->fetch(PDO::FETCH_ASSOC) ?: [];
      }

      function getPendingDeliveries($pdo, $outletId)
      {
        $stmt = $pdo->prepare("
        SELECT * FROM delivery_schedule 
        WHERE outlet_id = ? AND status IN ('scheduled', 'in_transit') 
        ORDER BY delivery_date LIMIT 5
    ");
        $stmt->execute([$outletId]);
        return $stmt->fetchAll(PDO::FETCH_ASSOC) ?: [];
      }

      function getTotalOrders($pdo, $outletId)
      {
        $stmt = $pdo->prepare("SELECT COUNT(*) FROM gas_requests WHERE outlet_id = ?");
        $stmt->execute([$outletId]);
        return $stmt->fetchColumn() ?: 0;
      }

      function getConfirmedOrders($pdo, $outletId)
      {
        $stmt = $pdo->prepare("SELECT COUNT(*) FROM gas_requests WHERE outlet_id = ? AND status = 'confirmed'");
